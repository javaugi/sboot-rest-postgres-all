package com.interview.codesignal.concurrency;

/**
 *
 * @author javaugi
 */
public class Fact {
    private String name;
    private Object value;

    public Fact(String name, Object value) {
        this.name = name;
        this.value = value;
    }

    public String getName() {
        return name;
    }

    public Object getValue() {
        return value;
    }

    @Override
    public String toString() {
        return name + "=" + value;
    }    
}
